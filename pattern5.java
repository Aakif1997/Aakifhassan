
public class pattern5 {
 public static void main(String[] args) {
	int n=4;
	for (int i=1;i<n;i++) {
		System.out.print(" ");
	}
	System.out.println();
 
    n=n+1;
    for(int i=1;i<=n;i++) {
    	for(int j=4;j>=1;j--) {
    		System.out.print(" * ");
    	}
    	System.out.println();
    }
    	}
}
  
    
    