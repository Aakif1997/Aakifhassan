
public class pattern4 {
	
	public static void main (String[] args) {
		int n=5;
		for (int i=1;i<=n;i++) {
			for (int j=n-1;j<=i;j++) {
					System.out.print("-");
			}
		for (int i1=0;i1<=n;i1++) {
			for (int j=1;j<=i1;j++) {
					System.out.print("*");
							}
          System.out.println();

}
	}
}
}

